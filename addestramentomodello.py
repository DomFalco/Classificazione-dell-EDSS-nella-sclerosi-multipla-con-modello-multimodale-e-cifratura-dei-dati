  def encrypt_data(data, password, output_file=None):
      # Converti i dati in JSON e poi in bytes
      if not isinstance(data, (str, bytes)):
          data = json.dumps(data, indent=2)
      if isinstance(data, str):
          data = data.encode('utf-8')

      # Genera un salt randomico
      salt = get_random_bytes(16)

      # Deriva la chiave dalla password usando PBKDF2
      key = PBKDF2(password, salt, 32, count=1000000)

      # Genera un nonce randomico
      nonce = get_random_bytes(12)

      # Crea il cifrario AES-GCM
      cipher = AES.new(key, AES.MODE_GCM, nonce=nonce)

      # Cifra i dati
      ciphertext, tag = cipher.encrypt_and_digest(data)

      # Prepara i dati cifrati
      encrypted_data = {
          'salt': base64.b64encode(salt).decode('utf-8'),
          'nonce': base64.b64encode(nonce).decode('utf-8'),
          'tag': base64.b64encode(tag).decode('utf-8'),
          'ciphertext': base64.b64encode(ciphertext).decode('utf-8')
      }

      # Salva i dati cifrati in un file se specificato
      if output_file:
          with open(output_file, 'w') as f:
              json.dump(encrypted_data, f)
          print(f"Dati cifrati salvati in: {output_file}")

      return encrypted_data

