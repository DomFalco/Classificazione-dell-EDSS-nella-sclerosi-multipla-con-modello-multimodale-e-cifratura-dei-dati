# Modello
class EnhancedCrossModalityAttention(nn.Module):
    def __init__(self, img_feat_dim, tab_feat_dim):
        super().__init__()
        self.img_proj = nn.Sequential(
            nn.Linear(img_feat_dim, img_feat_dim),
            nn.BatchNorm1d(img_feat_dim),
            nn.LeakyReLU(0.1)
        )
        self.tab_proj = nn.Sequential(
            nn.Linear(tab_feat_dim, img_feat_dim),
            nn.BatchNorm1d(img_feat_dim),
            nn.LeakyReLU(0.1)
        )
        self.attention = nn.Sequential(
            nn.Linear(img_feat_dim * 2, img_feat_dim),
            nn.LeakyReLU(0.1),
            nn.Linear(img_feat_dim, img_feat_dim),
            nn.Sigmoid()
        )

    def forward(self, img_features, tab_features):
        img_proj = self.img_proj(img_features)
        tab_proj = self.tab_proj(tab_features)
        combined = torch.cat([img_proj, tab_proj], dim=1)
        attention_weights = self.attention(combined)
        return attention_weights * img_proj + (1 - attention_weights) * tab_proj

#classe per classificatore ibrido
class EnhancedHybridEDSSClassifier(nn.Module):
    def __init__(self, num_tabular_features=6, num_classes=3):
        super().__init__()

        # Branch immagini con maggiore regolarizzazione
        self.cnn = nn.Sequential(
            nn.Conv2d(1, 32, 3, padding=1), # 1° strato convoluzionale
            nn.BatchNorm2d(32), #normalizzazione
            nn.LeakyReLU(0.1), #attivazione
            nn.MaxPool2d(2), #downsampling
            nn.Dropout(0.3), #regolarizzazione

            nn.Conv2d(32, 64, 3, padding=1), # 2° strato convoluzionale
            nn.BatchNorm2d(64),
            nn.LeakyReLU(0.1),
            nn.MaxPool2d(2),
            nn.Dropout(0.4),

            nn.Conv2d(64, 128, 3, padding=1), # 3° strato convoluzionale
            nn.BatchNorm2d(128),
            nn.LeakyReLU(0.1),
            nn.AdaptiveAvgPool2d((8, 8)), #pooling adattivo
            nn.Dropout(0.5)
        )

        # Branch tabellare
        self.tab_fc = nn.Sequential(
            nn.Linear(num_tabular_features, 64), # 1° strato convoluzionale
            nn.BatchNorm1d(64),
            nn.LeakyReLU(0.1),
            nn.Dropout(0.4),

            nn.Linear(64, 128), # 2° strato convoluzionale
            nn.BatchNorm1d(128),
            nn.LeakyReLU(0.1),
            nn.Dropout(0.5)
        )

        self.attention = EnhancedCrossModalityAttention(img_feat_dim=128*8*8, tab_feat_dim=128)

        # Classificatore con maggiore regolarizzazione
        self.classifier = nn.Sequential(
            nn.Linear(128*8*8, 256), #riduzione dimensionale
            nn.BatchNorm1d(256),
            nn.LeakyReLU(0.1),
            nn.Dropout(0.6), #regolarizzazione
            nn.Linear(256, num_classes) #output
        )

        self._initialize_weights()

    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='leaky_relu')
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.Linear):
                nn.init.xavier_normal_(m.weight)
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)

    def forward(self, img, tab_data):
        img_features = self.cnn(img).view(img.size(0), -1) #estrazione e appiattimento features immagine
        tab_features = self.tab_fc(tab_data) #elaborazione dati tabellari
        fused_features = self.attention(img_features, tab_features) #fusione
        return self.classifier(fused_features) #classificazione finale

class FocalLoss(nn.Module):
    def __init__(self, alpha=None, gamma=2, reduction='mean'):
        super().__init__()
        self.alpha = alpha
        self.gamma = gamma
        self.reduction = reduction

    def forward(self, inputs, targets):
        ce_loss = F.cross_entropy(inputs, targets, reduction='none')
        pt = torch.exp(-ce_loss)
        focal_loss = (1 - pt)**self.gamma * ce_loss

        if self.alpha is not None:
            focal_loss = self.alpha[targets] * focal_loss

        if self.reduction == 'mean':
            return focal_loss.mean()
        elif self.reduction == 'sum':
            return focal_loss.sum()
        return focal_loss

