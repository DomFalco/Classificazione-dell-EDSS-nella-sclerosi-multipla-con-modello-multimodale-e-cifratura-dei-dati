import os
import cv2
import numpy as np
import tenseal as ts
from tqdm import tqdm
import json

# Configurazione
ENCRYPTED_FOLDER = "/content/drive/Shareddrives/ProgettoBigData/dataset_slices_encrypted"
DECRYPTED_FOLDER = "/content/drive/Shareddrives/ProgettoBigData/dataset_slices_decrypted"
CHECKPOINT_FILE = os.path.join(DECRYPTED_FOLDER, "decryption_checkpoint.json")
CONTEXT_PATH = os.path.join(ENCRYPTED_FOLDER, "tenseal_context.seal")

def load_checkpoint():
    if os.path.exists(CHECKPOINT_FILE):
        try:
            with open(CHECKPOINT_FILE, 'r') as f:
                return json.load(f)
        except:
            return {"processed_files": []}
    return {"processed_files": []}

def save_checkpoint(processed_files):
    os.makedirs(DECRYPTED_FOLDER, exist_ok=True)
    with open(CHECKPOINT_FILE, 'w') as f:
        json.dump({"processed_files": processed_files}, f)

def decrypt_all_images():
    # Verifiche preliminari
    if not os.path.exists(CONTEXT_PATH):
        raise FileNotFoundError("Contesto di cifratura non trovato!")

    os.makedirs(DECRYPTED_FOLDER, exist_ok=True)

    # Carica il contesto
    with open(CONTEXT_PATH, "rb") as f:
        context = ts.context_from(f.read())

    # Lista file cifrati
    encrypted_files = [f for f in os.listdir(ENCRYPTED_FOLDER) if f.endswith('.enc')]

    if not encrypted_files:
        print("Nessun file cifrato trovato!")
        return

    # Carica checkpoint
    checkpoint = load_checkpoint()
    processed_files = set(checkpoint["processed_files"])
    files_to_process = [f for f in encrypted_files if f not in processed_files]

    if not files_to_process:
        print("Tutti i file sono già stati processati!")
        return

    print(f"Trovati {len(encrypted_files)} file cifrati. {len(files_to_process)} da processare...")

    success_count = 0
    for filename in tqdm(files_to_process, desc="Decifrazione"):
        try:
            input_path = os.path.join(ENCRYPTED_FOLDER, filename)
            output_path = os.path.join(DECRYPTED_FOLDER, filename.replace('.enc', '.png'))

            # Carica e decifra
            with open(input_path, "rb") as f:
                encrypted = ts.ckks_tensor_from(context, f.read())

            # Conversione esplicita a NumPy
            decrypted = np.array(encrypted.decrypt().tolist())  # Converti prima in lista

            # Normalizzazione e conversione a immagine
            if decrypted.min() < 0 or decrypted.max() > 1:
                decrypted = (decrypted - decrypted.min()) / (decrypted.max() - decrypted.min())

            decrypted = (decrypted * 255).clip(0, 255).astype(np.uint8)

            # Controllo dimensioni
            if decrypted.ndim == 1:
                size = int(np.sqrt(decrypted.shape[0]))
                decrypted = decrypted.reshape((size, size))

            # Salva l'immagine
            cv2.imwrite(output_path, decrypted)
            success_count += 1

            # SALVA CHECKPOINT DOPO OGNI FILE
            processed_files.add(filename)
            save_checkpoint(list(processed_files))

        except Exception as e:
            print(f"\nErrore su {filename}: {type(e).__name__} - {str(e)}")
            continue
        except KeyboardInterrupt:
            print("\nInterruzione rilevata! Checkpoint salvato.")
            save_checkpoint(list(processed_files))
            break

    # Report finale
    print(f"\nDecifrate con successo {success_count}/{len(files_to_process)} immagini")
    if success_count > 0:
        print(f"Immagini salvate in: {DECRYPTED_FOLDER}")

if __name__ == "__main__":
    decrypt_all_images()