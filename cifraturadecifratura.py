def decrypt_dataset(encrypted_file, password, output_csv='/content/drive/Shareddrives/ProgettoBigData/Patient_Info_Extended_Decrypted.csv'):

    # Carica i dati cifrati
    with open(encrypted_file, 'r') as f:
        encrypted_data = json.load(f)

    # Decodifica i componenti
    salt = base64.b64decode(encrypted_data['salt'])
    nonce = base64.b64decode(encrypted_data['nonce'])
    tag = base64.b64decode(encrypted_data['tag'])
    ciphertext = base64.b64decode(encrypted_data['ciphertext'])

    # Deriva la chiave dalla password
    key = PBKDF2(password, salt, 32, count=1000000)

    # Crea il cifrario per decifrare
    cipher = AES.new(key, AES.MODE_GCM, nonce=nonce)

    # Decifra i dati
    try:
        decrypted_data = cipher.decrypt_and_verify(ciphertext, tag)

        # Converti i dati decifrati in JSON
        data_json = decrypted_data.decode('utf-8')

        # Converti JSON in dataframe usando StringIO
        df = pd.read_json(StringIO(data_json))

        # Salva il dataset decifrato
        df.to_csv(output_csv, sep=';', index=False, quoting=csv.QUOTE_ALL)

        print(f"Dataset decifrato salvato in: {output_csv}")
        return df

    except ValueError:
        print("Errore: Password errata o dati corrotti")
        return None

