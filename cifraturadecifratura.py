import numpy as np
import tenseal as ts
from tqdm import tqdm
import pickle

# Configurazione dei percorsi
input_folder = "/content/drive/Shareddrives/ProgettoBigData/dataset_slices"
output_folder = "/content/drive/Shareddrives/ProgettoBigData/dataset_slices_encrypted"
checkpoint_file = os.path.join(output_folder, "checkpoint.pkl")

# Creazione del contesto TenSEAL per la cifratura CKKS
def create_context():
    context = ts.context(
        ts.SCHEME_TYPE.CKKS, # Schema CKKS per cifratura omomorfica
        poly_modulus_degree=4096,  # Grado del polinomio (impatta sicurezza e performance)
        coeff_mod_bit_sizes=[30, 20, 30] # Imposta lo scale globale per la precisione
    )
    context.generate_galois_keys()
    context.global_scale = 2**20
    return context

# Caricamento, normalizzazione e cifratura di un'immagine
def process_and_encrypt(context, img_path):
    img = cv2.imread(img_path, cv2.IMREAD_GRAYSCALE) # Legge l'immagine in scala di grigi
    img = cv2.resize(img, (64, 64)) # Ridimensiona a 64x64 pixe
    img = cv2.normalize(img, None, alpha=0, beta=1, norm_type=cv2.NORM_MINMAX, dtype=cv2.CV_32F)
    encrypted = ts.ckks_tensor(context, img)  # Cifra l'immagine come tensore CKKS
    return encrypted

# Caricamento dello stato di checkpoint (file già processati)
def load_checkpoint():
    if os.path.exists(checkpoint_file):
        with open(checkpoint_file, "rb") as f:
            return pickle.load(f)
    # Se il checkpoint non esiste, inizializza lo stato
    return {
        'processed_files': set(), # Set di file già cifrati
        'context_saved': False # Flag per sapere se il contesto è già stato salvato
    }

# Salvataggio dello stato di checkpoint
def save_checkpoint(state):
    with open(checkpoint_file, "wb") as f:
        pickle.dump(state, f)

# Funzione principale per cifrare tutte le immagini nella cartella
def encrypt_entire_folder():
    os.makedirs(output_folder, exist_ok=True) # Crea la cartella di output se non esiste
    checkpoint = load_checkpoint() # Carica lo stato precedente

    # Se il contesto non è stato ancora salvato, lo crea e lo salva
    if not checkpoint['context_saved']:
        context = create_context()
        context_path = os.path.join(output_folder, "tenseal_context.seal")
        with open(context_path, "wb") as f:
            f.write(context.serialize(save_secret_key=True))
        checkpoint['context_saved'] = True
        save_checkpoint(checkpoint)
    # Altrimenti lo carica dal file salvato
    else:
        context_path = os.path.join(output_folder, "tenseal_context.seal")
        with open(context_path, "rb") as f:
            context = ts.context_from(f.read())

    # Elenco dei file immagine nella cartella di input
    image_files = [f for f in os.listdir(input_folder)
                 if f.lower().endswith(('.png', '.jpg', '.jpeg', '.tif', '.tiff'))]

    # Filtra solo i file non ancora processati
    remaining_files = [f for f in image_files if f not in checkpoint['processed_files']]

    # Cifratura dei file rimanenti
    for filename in tqdm(remaining_files, desc="Cifratura immagini mediche"):
        input_path = os.path.join(input_folder, filename)

        # Rinomina sicura del file (evita problemi con virgole)
        safe_filename = filename.replace(",", ".")
        output_path = os.path.join(output_folder, f"{os.path.splitext(safe_filename)[0]}.enc")

        try:
            if not os.path.exists(input_path):
                print(f"File di input non trovato: {input_path}")
                continue

            encrypted_img = process_and_encrypt(context, input_path)

            # Salva il file cifrato
            with open(output_path, "wb") as f:
                f.write(encrypted_img.serialize())

            # Aggiorna il checkpoint
            checkpoint['processed_files'].add(filename)
            save_checkpoint(checkpoint)

        except Exception as e:
            print(f"\nErrore su {filename}: {type(e).__name__} - {str(e)}")
            continue

    if len(remaining_files) == 0:
        print("Tutte le immagini sono già state processate.")
    else:
        print(f"\nCifratura completata. File salvati in: {output_folder}")

if __name__ == "__main__":
    encrypt_entire_folder()

